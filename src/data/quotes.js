// Get a quote for a specific date (deterministic based on date)
export function getQuoteForDate(dateStr) {
  const date = new Date(dateStr);
  // Use day of year as index to cycle through quotes
  const startOfYear = new Date(date.getFullYear(), 0, 0);
  const diff = date - startOfYear;
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  const quoteIndex = dayOfYear % MOTIVATIONAL_QUOTES.length;
  return MOTIVATIONAL_QUOTES[quoteIndex];
}

// Get today's quote
export function getTodaysQuote() {
  const today = new Date().toISOString().split('T')[0];
  return getQuoteForDate(today);
}
